l1 = [1,23,454,675,87,976,443,89,87]

# Arrange with ascending order 

l1.sort()
print(l1)

# Arrange with descending order

l1.reverse()
print(l1)

# Add variable at the end of list

l1.append(56)
print(l1)

# Add variable with index in list

l1.insert(3,290)
print(l1)

# Delete variable with index

l1.pop(2)
print(l1)

# Delete  all variable in the list

l1.remove(87)
print(l1)


