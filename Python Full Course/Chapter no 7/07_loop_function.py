# The Break Function

for i in range(2,50):
    if(i==39):
        break
    print(i)
   
# The Continue Function

for i in range(2,50):
    if i==39 :
        continue
    print(i)


# Pass statment

for i in range(2,50):
    if(i==39):
        pass

